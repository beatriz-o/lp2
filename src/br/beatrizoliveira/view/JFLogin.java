    private boolean verificarCampos(){
        if(!jTextField_Usuario.getText().equalsIgnoreCase("")&&
                !jPasswordField_senhausuario.getText().equalsIgnoreCase("")){
        return true;
    }else if(jTextField_Usuario.getText().equalsIgnoreCase("")&&
                !jPasswordField_senhausuario.getText().equalsIgnoreCase("")){
                jTextField_Usuario.setBorder(new LineBorder(Color.RED));
            return false;
        }else if(!jTextField_Usuario.getText().equalsIgnoreCase("")&&
                jPasswordField_senhausuario.getText().equalsIgnoreCase("")){
                jPasswordField_senhausuario.setBorder(new LineBorder(Color.RED));
                return false;
        }else {
             jTextField_Usuario.setBorder(new LineBorder(Color.RED));
             jPasswordField_senhausuario.setBorder(new LineBorder(Color.RED));
             return false;
        }
    }

