    private void jButton_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_entrarActionPerformed
        // TODO add your handling code here:
        
        if(verificarCampos()){
            try {
            usuario = DAO.autenticar(jTextField_Usuario.getText(), jPasswordField_senhausuario.getText());
            if(usuario!=null){
                if(usuario.getUsuario().equalsIgnoreCase(jTextField_Usuario.getText())&&
                        usuario.getSenha().equalsIgnoreCase(jPasswordField_senhausuario.getText())){
                        JFPrincipal jFPrincipal = new JFPrincipal();
                        jFPrincipal.setVisible(true);
                        this.dispose();
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Usuário ou senha incorretos");

            }
            } catch (Exception ex) {
                Logger.getLogger(JFLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton_entrarActionPerformed

